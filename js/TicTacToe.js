/**
 * @param {number} n
 */
class TicTacToe {
  constructor(n) {
    this.n = n;
    this.rows = new Array(n).fill(0);
    this.cols = new Array(n).fill(0);
    this.diagonal = 0;
    this.antiDiagonal = 0;
  }

  /** 
   * @param {number} row 
   * @param {number} col 
   * @param {number} player
   * @return {number}
   */
  move(row, col, player) {
    const update = player === 1 ? 1 : -1;

    this.rows[row] += update;
    this.cols[col] += update;
    if (row === col) this.diagonal += update;
    if (col === this.n - row - 1) this.antiDiagonal += update;

    if (
      Math.abs(this.rows[row]) === this.n ||
      Math.abs(this.cols[col]) === this.n ||
      Math.abs(this.diagonal) === this.n ||
      Math.abs(this.antiDiagonal) === this.n
    ) return player;

    return 0;
  }
}


